void CMsNatural::Draw(CDC *pDC, COLORREF c, int x, int y)
{
	CPen pen,*oldpen;

	y += 4;
	pen.CreatePen(PS_SOLID,1,c);
	oldpen = pDC->SelectObject(&pen);
	pDC->MoveTo(x-3,y-5);
	pDC->LineTo(x-3,y+3);
	pDC->LineTo(x+3,y+2);
	pDC->MoveTo(x+3,y+4);
	pDC->LineTo(x+3,y-3);
	pDC->LineTo(x-3,y-2);
	pDC->SelectObject(oldpen);
}

